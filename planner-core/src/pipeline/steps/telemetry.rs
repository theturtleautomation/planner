// Internal helpers
// ---------------------------------------------------------------------------

fn build_telemetry_context(
    factory_output: &FactoryOutputV1,
    satisfaction: &SatisfactionResultV1,
    budget: &RunBudgetV1,
) -> serde_json::Value {
    let failed_scenarios: Vec<&ScenarioResult> = satisfaction
        .scenario_results
        .iter()
        .filter(|r| !r.majority_pass)
        .collect();

    serde_json::json!({
        "build_status": format!("{:?}", factory_output.build_status),
        "nodes_completed": factory_output.node_results.iter()
            .filter(|n| n.success)
            .map(|n| &n.node_name)
            .collect::<Vec<_>>(),
        "nodes_failed": factory_output.node_results.iter()
            .filter(|n| !n.success)
            .map(|n| &n.node_name)
            .collect::<Vec<_>>(),
        "satisfaction_summary": satisfaction.user_message(),
        "gates_passed": satisfaction.gates_passed,
        "budget_status": format!("{:?}", budget.status),
        "budget_used_pct": if budget.hard_cap_usd > 0.0 {
            (budget.current_spend_usd / budget.hard_cap_usd * 100.0) as u32
        } else {
            0
        },
        "failed_scenario_count": failed_scenarios.len(),
        "failed_error_categories": failed_scenarios.iter()
            .filter_map(|r| r.generalized_error.as_ref())
            .map(|e| &e.category)
            .collect::<Vec<_>>(),
    })
}

/// Parsed telemetry response from Haiku.
#[derive(Debug)]
struct TelemetryLlmReport {
    headline: String,
    summary: String,
    needs_user_action: bool,
}

#[derive(Debug, serde::Deserialize)]
struct TelemetryJson {
    headline: String,
    summary: String,
    #[serde(default)]
    needs_user_action: bool,
    #[allow(dead_code)]
    action_description: Option<String>,
}

fn parse_telemetry_response(content: &str) -> StepResult<TelemetryLlmReport> {
    let cleaned = super::intake::strip_code_fences(content);

    let json: TelemetryJson = serde_json::from_str(&cleaned).map_err(|e| {
        StepError::JsonError(format!(
            "Failed to parse Telemetry Presenter response: {}. Raw: {}",
            e,
            &content[..content.len().min(300)]
        ))
    })?;

    Ok(TelemetryLlmReport {
        headline: json.headline,
        summary: json.summary,
        needs_user_action: json.needs_user_action,
    })
}

