/// Generate a telemetry report from factory output and validation results.
///
/// Phase 0: Uses Haiku for translation. No Docker sandbox (just output path).
pub async fn execute_telemetry_presentation(
    router: &LlmRouter,
    factory_output: &FactoryOutputV1,
    satisfaction: &SatisfactionResultV1,
    budget: &RunBudgetV1,
    project_id: Uuid,
) -> StepResult<TelemetryReport> {
    tracing::info!("Telemetry Presenter: translating results to plain English");

    // Build the context for Haiku
    let context = build_telemetry_context(factory_output, satisfaction, budget);

    let request = CompletionRequest {
        system: Some(TELEMETRY_SYSTEM_PROMPT.to_string()),
        messages: vec![Message {
            role: Role::User,
            content: format!(
                "Translate these factory results into plain English:\n\n{}",
                serde_json::to_string_pretty(&context).unwrap_or_default()
            ),
        }],
        max_tokens: 1024,
        temperature: 0.4,
        model: DefaultModels::TELEMETRY_PRESENTER.to_string(),
    };

    let response = router.complete(request).await?;
    let llm_report = parse_telemetry_response(&response.content)?;

    // Generate consequence cards if needed
    let mut consequence_cards = Vec::new();

    // Budget warning card
    if budget.status == BudgetStatus::Warning {
        consequence_cards.push(ConsequenceCardV1 {
            card_id: Uuid::new_v4(),
            project_id,
            trigger: CardTrigger::BudgetWarning,
            problem: format!(
                "This run has used ${:.2} of its ${:.2} budget.",
                budget.current_spend_usd, budget.hard_cap_usd,
            ),
            proposed_solution: "The run can continue, but you may want to review before it reaches the limit.".into(),
            impact: "If the budget runs out, the work will stop and you'll see what was completed.".into(),
            actions: vec![
                CardAction {
                    label: "Continue".into(),
                    description: "Let the run keep going within the current budget.".into(),
                },
                CardAction {
                    label: "Stop".into(),
                    description: "Stop the run now and review what's been done.".into(),
                },
            ],
            status: CardStatus::Pending,
            resolution: None,
        });
    }

    // Budget exhausted card
    if budget.status == BudgetStatus::HardStop {
        consequence_cards.push(ConsequenceCardV1 {
            card_id: Uuid::new_v4(),
            project_id,
            trigger: CardTrigger::BudgetExhausted,
            problem: format!(
                "This run reached its ${:.2} budget limit.",
                budget.hard_cap_usd,
            ),
            proposed_solution: "Here's what was completed. Want to continue with a new budget allocation?".into(),
            impact: "No more work will be done until you approve additional budget.".into(),
            actions: vec![
                CardAction {
                    label: "Add Budget".into(),
                    description: "Allocate more budget and continue from where we left off.".into(),
                },
                CardAction {
                    label: "Review First".into(),
                    description: "Let me see what was built before deciding.".into(),
                },
            ],
            status: CardStatus::Pending,
            resolution: None,
        });
    }

    // Critical gate failure card
    if satisfaction.critical_pass_rate < 1.0 {
        consequence_cards.push(ConsequenceCardV1 {
            card_id: Uuid::new_v4(),
            project_id,
            trigger: CardTrigger::CriticalGateFailure,
            problem: "Something critical didn't work as expected.".into(),
            proposed_solution: "I need to understand your intent better before trying again.".into(),
            impact: "The core functionality needs to work before we move forward.".into(),
            actions: vec![
                CardAction {
                    label: "Clarify".into(),
                    description: "Help me understand what should happen differently.".into(),
                },
                CardAction {
                    label: "Retry".into(),
                    description: "Try building it again with the same requirements.".into(),
                },
            ],
            status: CardStatus::Pending,
            resolution: None,
        });
    }

    // High gate failure card (only when critical passes but high fails)
    if satisfaction.critical_pass_rate >= 1.0 && satisfaction.high_pass_rate < 0.95 {
        // Collect the High-tier error categories for context
        let high_error_cats: Vec<&str> = satisfaction
            .scenario_results
            .iter()
            .filter(|r| r.tier == ScenarioTier::High && !r.majority_pass)
            .filter_map(|r| r.generalized_error.as_ref())
            .map(|e| e.category.as_str())
            .collect();

        let problem_detail = if high_error_cats.is_empty() {
            "Some important behaviors didn't work as expected.".into()
        } else {
            format!(
                "Some important behaviors didn't work as expected (areas: {}).",
                high_error_cats.join(", "),
            )
        };

        consequence_cards.push(ConsequenceCardV1 {
            card_id: Uuid::new_v4(),
            project_id,
            trigger: CardTrigger::HighGateFailure,
            problem: problem_detail,
            proposed_solution: "The core features work, but some important pieces need attention. I can retry or you can guide me.".into(),
            impact: "The app is mostly functional but some key behaviors are missing or incorrect.".into(),
            actions: vec![
                CardAction {
                    label: "Retry".into(),
                    description: "Let me try building those parts again.".into(),
                },
                CardAction {
                    label: "Clarify".into(),
                    description: "Tell me more about how these features should work.".into(),
                },
                CardAction {
                    label: "Accept".into(),
                    description: "It's good enough — move forward with what's built.".into(),
                },
            ],
            status: CardStatus::Pending,
            resolution: None,
        });
    }

    let satisfaction_message = satisfaction.user_message().to_string();
    let needs_user_action = llm_report.needs_user_action
        || !consequence_cards.is_empty()
        || !satisfaction.gates_passed;

    let preview_path = if factory_output.build_status == BuildStatus::Success
        || factory_output.build_status == BuildStatus::PartialSuccess
    {
        Some(factory_output.output_path.clone())
    } else {
        None
    };

    Ok(TelemetryReport {
        headline: llm_report.headline,
        summary: llm_report.summary,
        preview_path,
        needs_user_action,
        consequence_cards,
        satisfaction_message,
    })
}

/// Build a non-technical telemetry report WITHOUT an LLM call.
/// Used as a fallback when Haiku is unavailable, or for testing.
pub fn build_telemetry_report_deterministic(
    factory_output: &FactoryOutputV1,
    satisfaction: &SatisfactionResultV1,
    budget: &RunBudgetV1,
    _project_id: Uuid,
) -> TelemetryReport {
    let headline = match factory_output.build_status {
        BuildStatus::Success => satisfaction.user_message().to_string(),
        BuildStatus::PartialSuccess => "Your app is mostly built, but some pieces need work.".into(),
        BuildStatus::Failed => "The build didn't complete. Let me figure out what went wrong.".into(),
        BuildStatus::BudgetExhausted => format!(
            "The run used its full ${:.2} budget. Here's what got done.",
            budget.hard_cap_usd,
        ),
        BuildStatus::Error { ref message } => format!("Something unexpected happened: {}", message),
    };

    let nodes_done = factory_output.node_results.iter().filter(|n| n.success).count();
    let nodes_total = factory_output.node_results.len();

    let summary = format!(
        "{} Built {} of {} components. {}",
        satisfaction.user_message(),
        nodes_done,
        nodes_total,
        if satisfaction.gates_passed {
            "Ready for your review."
        } else {
            "Some things need attention before we continue."
        },
    );

    TelemetryReport {
        headline,
        summary,
        preview_path: Some(factory_output.output_path.clone()),
        needs_user_action: !satisfaction.gates_passed,
        consequence_cards: vec![],
        satisfaction_message: satisfaction.user_message().to_string(),
    }
}

