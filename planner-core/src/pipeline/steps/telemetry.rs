//! # Telemetry Presenter — Factory Output → Plain English
//!
//! The Telemetry Presenter has three responsibilities:
//!
//! 1. **Factory Question Classification**: Classify escalations as
//!    intent gaps (→ Consequence Card) or implementation details
//!    (→ autonomous resolution).
//!
//! 2. **Satisfaction Score Translation**: Convert tiered pass rates
//!    into plain-English messages the Smart Tinkerer can understand.
//!    The user NEVER sees numeric scores.
//!
//! 3. **Consequence Cards**: Generate cards for Critical gate failures,
//!    High gate failures, budget warnings, and budget exhaustion.
//!
//! 4. **Live Preview**: (Phase 2+) Serve factory output in Docker sandbox.
//!    Phase 1: Just report the output path.
//!
//! Model: claude-haiku-4-5 (fast, cheap plain-English translation)

use uuid::Uuid;

use crate::llm::{CompletionRequest, DefaultModels, Message, Role};
use crate::llm::providers::LlmRouter;
use planner_schemas::*;
use super::{StepResult, StepError};

// ---------------------------------------------------------------------------
// Telemetry Report — the output of the Telemetry Presenter
// ---------------------------------------------------------------------------

/// Plain-English telemetry report for the Smart Tinkerer.
#[derive(Debug, Clone)]
pub struct TelemetryReport {
    /// The headline message for the user.
    pub headline: String,

    /// Detailed plain-English summary of what happened.
    pub summary: String,

    /// Path where the built app lives (for Live Preview in Phase 1+).
    pub preview_path: Option<String>,

    /// Whether the user needs to take action.
    pub needs_user_action: bool,

    /// Consequence cards generated by this round (if any).
    pub consequence_cards: Vec<ConsequenceCardV1>,

    /// Satisfaction message from the validator.
    pub satisfaction_message: String,
}

// ---------------------------------------------------------------------------
// System prompt for Haiku-driven translation
// ---------------------------------------------------------------------------

const TELEMETRY_SYSTEM_PROMPT: &str = r#"You are the Telemetry Presenter for Planner v2. Your job: translate factory execution results into plain English for a non-technical user (the "Smart Tinkerer").

## Rules
1. NEVER show numeric scores, percentages, or technical metrics to the user
2. Use warm, clear language — like explaining results to a smart friend
3. Focus on WHAT HAPPENED and WHAT IT MEANS, not technical details
4. If something failed, explain what didn't work in terms the user understands
5. If the user needs to act, be specific about what you need from them
6. Keep it under 200 words

## Output Format
Respond with ONLY a JSON object (no markdown fences):

{
  "headline": "One-sentence summary (like an email subject line)",
  "summary": "2-4 sentence plain-English explanation of what happened and what comes next",
  "needs_user_action": true|false,
  "action_description": "What the user should do (null if needs_user_action is false)"
}"#;

