#[cfg(test)]
mod tests {
    use super::*;

    fn sample_factory_output() -> FactoryOutputV1 {
        // Create a temp dir for the test
        let output_dir = std::env::temp_dir()
            .join("planner-git-test")
            .join(Uuid::new_v4().to_string());
        std::fs::create_dir_all(&output_dir).unwrap();

        // Write a test file
        std::fs::write(output_dir.join("index.html"), "<html>test</html>").unwrap();

        FactoryOutputV1 {
            kilroy_run_id: Uuid::new_v4(),
            nlspec_version: "1.0".into(),
            attempt: 1,
            build_status: BuildStatus::Success,
            spend_usd: 0.42,
            checkpoint_path: "/tmp/cp.json".into(),
            dod_results: vec![],
            node_results: vec![],
            output_path: output_dir.to_string_lossy().to_string(),
        }
    }

    #[tokio::test]
    async fn git_projection_creates_commit() {
        let factory_output = sample_factory_output();
        let project_id = Uuid::new_v4();

        let result = execute_git_projection(
            &factory_output,
            project_id,
            "Test Widget",
            "test-widget",
        )
        .await;

        assert!(result.is_ok());
        let projection = result.unwrap();
        assert_eq!(projection.commit.project_id, project_id);
        assert_eq!(projection.commit.branch, "main");
        assert!(!projection.commit.commit_hash.is_empty());
        assert!(projection.commit.message.contains("test-widget"));

        // Cleanup
        let _ = std::fs::remove_dir_all(&factory_output.output_path);
    }

    #[test]
    fn simulated_projection_produces_valid_result() {
        let factory_output = FactoryOutputV1 {
            kilroy_run_id: Uuid::new_v4(),
            nlspec_version: "1.0".into(),
            attempt: 1,
            build_status: BuildStatus::Success,
            spend_usd: 0.0,
            checkpoint_path: String::new(),
            dod_results: vec![],
            node_results: vec![],
            output_path: "/tmp/test-output".into(),
        };

        let result = simulate_git_projection(
            &factory_output,
            Uuid::new_v4(),
            "Test Project",
            "test-project",
        );

        assert_eq!(result.commit.branch, "main");
        assert!(result.commit.commit_hash.len() == 40);
        assert!(result.commit.message.contains("simulated"));
    }
}
